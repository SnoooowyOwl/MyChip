def emit_cpu_runtime_helpers(e: Rv32Emitter) -> None:
    """Emit CPU helper labels that are specific to this CNN schedule."""

    e.comment("CNN schedule helper subroutines.")
    e.label("acc_store_packed_13")
    e.inst("lw", "t0", f"{OFF_RES_PACK_0}(s0)")
    e.inst("sw", "t0", "0(a0)")
    e.inst("lw", "t0", f"{OFF_RES_PACK_1}(s0)")
    e.inst("sw", "t0", "4(a0)")
    e.inst("lw", "t0", f"{OFF_RES_PACK_2}(s0)")
    e.inst("sw", "t0", "8(a0)")
    e.inst("lw", "t0", f"{OFF_RES_PACK_3}(s0)")
    e.inst("sb", "t0", "12(a0)")
    e.ret()
    e.emit()

    e.label("acc_accumulate_raw_11")
    e.inst("li", "t0", "0")
    e.label("acc_accumulate_raw_11_loop")
    e.inst("li", "t1", "11")
    e.inst("bge", "t0", "t1", "acc_accumulate_raw_11_done")
    e.inst("slli", "t2", "t0", "2")
    e.inst("add", "t3", "a0", "t2")
    e.inst("lw", "t4", "0(t3)")
    e.inst("li", "t5", str(OFF_RAW_BASE))
    e.inst("add", "t5", "t5", "t2")
    e.inst("add", "t6", "s0", "t5")
    e.inst("lw", "t5", "0(t6)")
    e.inst("add", "t4", "t4", "t5")
    e.inst("sw", "t4", "0(t3)")
    e.inst("addi", "t0", "t0", "1")
    e.inst("j", "acc_accumulate_raw_11_loop")
    e.label("acc_accumulate_raw_11_done")
    e.ret()
    e.emit()

    e.label("zero_words")
    e.inst("beq", "a1", "zero", "zero_words_done")
    e.label("zero_words_loop")
    e.inst("sw", "zero", "0(a0)")
    e.inst("addi", "a0", "a0", "4")
    e.inst("addi", "a1", "a1", "-1")
    e.inst("bne", "a1", "zero", "zero_words_loop")
    e.label("zero_words_done")
    e.ret()
    e.emit()

    e.label("postprocess_accum_11")
    e.inst("li", "t0", "0")
    e.label("postprocess_accum_11_loop")
    e.inst("li", "t1", "11")
    e.inst("bge", "t0", "t1", "postprocess_accum_11_done")
    e.inst("slli", "t2", "t0", "2")
    e.inst("add", "t3", "a0", "t2")
    e.inst("lw", "t4", "0(t3)")
    e.inst("blt", "t4", "zero", "postprocess_accum_11_zero")
    e.inst("andi", "t4", "t4", "255")
    e.inst("j", "postprocess_accum_11_store")
    e.label("postprocess_accum_11_zero")
    e.inst("li", "t4", "0")
    e.label("postprocess_accum_11_store")
    e.inst("add", "t5", "a1", "t0")
    e.inst("sb", "t4", "0(t5)")
    e.inst("addi", "t0", "t0", "1")
    e.inst("j", "postprocess_accum_11_loop")
    e.label("postprocess_accum_11_done")
    e.ret()
    e.emit()

    e.label("fill_fc_scratch_conv2")
    e.inst("li", "t0", "0")
    e.la("t1", "fc_scratch_offsets")
    e.la("t2", "conv2_flat_offsets")
    e.label("fill_fc_scratch_conv2_loop")
    e.inst("li", "t3", "36")
    e.inst("bge", "t0", "t3", "fill_fc_scratch_conv2_done")
    e.inst("add", "t4", "a0", "t0", comment="global feature index")
    e.inst("lbu", "t5", "0(t1)", comment="scratch byte offset")
    e.inst("add", "t6", "s6", "t5")
    e.inst("li", "t3", "132")
    e.inst("bge", "t4", "t3", "fill_fc_scratch_conv2_zero")
    e.inst("slli", "t5", "t4", "1")
    e.inst("add", "t5", "t2", "t5")
    e.inst("lhu", "t5", "0(t5)")
    e.inst("add", "t5", "s3", "t5")
    e.inst("lbu", "t5", "0(t5)")
    e.inst("j", "fill_fc_scratch_conv2_store")
    e.label("fill_fc_scratch_conv2_zero")
    e.inst("li", "t5", "0")
    e.label("fill_fc_scratch_conv2_store")
    e.inst("sb", "t5", "0(t6)")
    e.inst("addi", "t0", "t0", "1")
    e.inst("addi", "t1", "t1", "1")
    e.inst("j", "fill_fc_scratch_conv2_loop")
    e.label("fill_fc_scratch_conv2_done")
    e.ret()
    e.emit()

    e.label("fill_fc_scratch_linear")
    e.inst("li", "t0", "0")
    e.la("t1", "fc_scratch_offsets")
    e.label("fill_fc_scratch_linear_loop")
    e.inst("li", "t2", "36")
    e.inst("bge", "t0", "t2", "fill_fc_scratch_linear_done")
    e.inst("lbu", "t3", "0(t1)")
    e.inst("add", "t4", "s6", "t3")
    e.inst("bge", "t0", "a1", "fill_fc_scratch_linear_zero")
    e.inst("add", "t5", "a0", "t0")
    e.inst("lbu", "t5", "0(t5)")
    e.inst("j", "fill_fc_scratch_linear_store")
    e.label("fill_fc_scratch_linear_zero")
    e.inst("li", "t5", "0")
    e.label("fill_fc_scratch_linear_store")
    e.inst("sb", "t5", "0(t4)")
    e.inst("addi", "t0", "t0", "1")
    e.inst("addi", "t1", "t1", "1")
    e.inst("j", "fill_fc_scratch_linear_loop")
    e.label("fill_fc_scratch_linear_done")
    e.ret()
    e.emit()


def emit_dcache_init(e: Rv32Emitter, rodata: dict[str, list[int]]) -> None:
    e.emit('    .section .dcache_init,"aw"')
    e.align(2)
    e.emit(f"    .org 0x{D_INPUT - DCACHE_BASE:x}")
    e.label("input0_padded")
    e.byte_values(rodata["input0_padded"])
    e.emit()
    e.align(2)
    e.emit(f"    .org 0x{D_CONST - DCACHE_BASE:x}")
    e.label("conv1_w_packed")
    e.word_values(rodata["conv1_w_packed"])
    e.emit()
    e.align(2)
    e.label("conv2_w_packed")
    e.word_values(rodata["conv2_w_packed"])
    e.emit()
    e.align(2)
    e.label("fc1_w_packed")
    e.word_values(rodata["fc1_w_packed"])
    e.emit()
    e.align(2)
    e.label("fc2_w_packed")
    e.word_values(rodata["fc2_w_packed"])
    e.emit()
    e.align(1)
    e.label("fc_scratch_offsets")
    e.byte_values(rodata["fc_scratch_offsets"])
    e.emit()
    e.align(1)
    e.label("conv2_flat_offsets")
    e.half_values(rodata["conv2_flat_offsets"])


def emit_assembly(
    rodata: dict[str, list[int]],
    sample_index: int | None = None,
    expected_output: int | None = None,
) -> str:
    e = Rv32Emitter()
    api = AcceleratorAPI(e)
    emit_header(e, sample_index=sample_index, expected_output=expected_output)
    emit_main(e, api)
    emit_run_conv1(e)
    emit_run_conv2(e)
    emit_run_fc1(e)
    emit_run_fc2(e)
    api.emit_runtime_helpers(dma_wait_nops=DMA_WAIT_NOPS)
    emit_cpu_runtime_helpers(e)
    emit_dcache_init(e, rodata)
    return e.text()
